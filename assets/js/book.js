  thisElement4 = $(this).attr("data-obj");
  innerContent4(thisElement4);
  $("#cariEbook").css({"display":"none"});
  $("#list-ebook").css({"display":"none"});
  $("#ebook-lainnya").css({"display":"none"});
  $("#daftar-ebook").css({"display":"block"});
